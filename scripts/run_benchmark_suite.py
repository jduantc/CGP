#!/usr/bin/env python
"""Benchmark heuristic CGP vs. mixture-EM graph-pair recovery, across many
true graph pairs (spanning the range of structural distinguishability) and
both linear and quadratic (nonlinear) SEM mechanisms.

Unlike the pipeline's bundled legacy datasets (data/synthetic/*), every
dataset here is generated by cgp.datagen with a KNOWN true pair, so accuracy
is unambiguous. See docs/notes/benchmark_suite.md for why this was necessary
and what it found.

Every generated dataset is saved to data/synthetic/benchmark_suite/<name>/
(merged.dat, true_subset1.dat, true_subset2.dat, meta.json) -- both as a
fixed, inspectable test set independent of any future change to
cgp.datagen's internal RNG usage, and so results/benchmark_suite.csv's rows
(joined by the "dataset" column) can be re-examined without re-running
generation.

Deliberately does not test all C(11,2)=55 candidate pairs as "true pairs" --
that would be ~3.5x the runtime here for diminishing signal. Instead it
samples pairs spanning every structural-distance bucket (1 through 5 edges
different) so results characterize accuracy vs. distinguishability. This
subsampling is intentional and logged below, not a silent cap.
"""

import csv
import json
import sys
import time
from pathlib import Path

import numpy as np

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from cgp.datagen import sample_mixture, structural_distance
from cgp.mixture import search_best_graph_set
from cgp.probing import THREE_NODE_EQUIVALENCE_CLASSES as CLASSES
from cgp.probing import optimize_graphs

# One representative true pair per structural distance bucket (see
# scripts/run_benchmark_suite.py docstring); chosen to span the full 1-5
# distance range the 11 equivalence classes admit.
TRUE_PAIRS = [
    (0, 1), (0, 2), (2, 6), (1, 9),          # distance 1 (hardest -- single edge apart)
    (0, 4), (0, 6), (1, 4), (3, 7),          # distance 2
    (0, 10), (1, 7), (2, 4), (3, 9),         # distance 3
    (4, 8), (5, 8), (7, 8),                  # distance 4
    (8, 10),                                 # distance 5 (easiest)
]
MECHANISMS = ["linear", "quadratic"]
SEEDS = [0, 1]
N_EACH = 600  # points per component; total dataset size = 2 * N_EACH

CGP_PARAMS = dict(nclusters=20, subset_size=300, nsubsets=300, ntrials=15)
EM_PARAMS = dict(n_iter=60, n_init=2)

REPO_ROOT = Path(__file__).resolve().parent.parent
DATA_DIR = REPO_ROOT / "data" / "synthetic" / "benchmark_suite"


def dataset_name(true_i, true_j, mechanism, seed):
    return f"pair{true_i:02d}-{true_j:02d}_{mechanism}_seed{seed}"


def save_dataset(name, merged, sub1, sub2, true_i, true_j, mechanism, seed):
    out_dir = DATA_DIR / name
    out_dir.mkdir(parents=True, exist_ok=True)
    header = "X1 X2 X3"
    np.savetxt(out_dir / "merged.dat", merged, header=header)
    np.savetxt(out_dir / "true_subset1.dat", sub1, header=header)
    np.savetxt(out_dir / "true_subset2.dat", sub2, header=header)
    meta = {
        "true_dag1": CLASSES[true_i], "true_dag2": CLASSES[true_j],
        "true_dag1_index": true_i, "true_dag2_index": true_j,
        "distance": structural_distance(CLASSES[true_i], CLASSES[true_j]),
        "mechanism": mechanism, "seed": seed, "n_each": N_EACH,
    }
    with open(out_dir / "meta.json", "w") as f:
        json.dump(meta, f, indent=2)


def run_one(true_i, true_j, mechanism, seed, save_data=True):
    rng = np.random.default_rng(seed * 1000 + true_i * 11 + true_j)
    merged, sub1, sub2 = sample_mixture(CLASSES[true_i], CLASSES[true_j], num_vars=3,
                                         n_each=N_EACH, mechanism=mechanism, rng=rng)

    if save_data:
        save_dataset(dataset_name(true_i, true_j, mechanism, seed), merged, sub1, sub2,
                      true_i, true_j, mechanism, seed)

    fars, _ = optimize_graphs(merged, candidate_graphs=CLASSES, rng=np.random.default_rng(seed), **CGP_PARAMS)
    flat = sorted((fars[a, b], a, b) for a in range(len(CLASSES)) for b in range(a))
    cgp_rank = next(k for k, (_, a, b) in enumerate(flat, 1) if {a, b} == {true_i, true_j})
    cgp_top1 = set(flat[0][1:]) == {true_i, true_j}

    em_results = search_best_graph_set(merged, CLASSES, set_size=2, rng=np.random.default_rng(seed), **EM_PARAMS)
    em_rank = next(k for k, (_, idxs, _) in enumerate(em_results, 1) if set(idxs) == {true_i, true_j})
    em_top1 = set(em_results[0][1]) == {true_i, true_j}

    return {
        "true_i": true_i, "true_j": true_j,
        "distance": structural_distance(CLASSES[true_i], CLASSES[true_j]),
        "mechanism": mechanism, "seed": seed,
        "dataset": dataset_name(true_i, true_j, mechanism, seed),
        "cgp_rank": cgp_rank, "cgp_top1_correct": int(cgp_top1),
        "em_rank": em_rank, "em_top1_correct": int(em_top1),
    }


def main():
    print(f"Benchmark suite: {len(TRUE_PAIRS)} true pairs x {len(MECHANISMS)} mechanisms x "
          f"{len(SEEDS)} seeds = {len(TRUE_PAIRS) * len(MECHANISMS) * len(SEEDS)} datasets\n"
          f"(sampling {len(TRUE_PAIRS)} of the 55 possible true pairs -- see module docstring)")

    rows = []
    t_start = time.perf_counter()
    for true_i, true_j in TRUE_PAIRS:
        for mechanism in MECHANISMS:
            for seed in SEEDS:
                row = run_one(true_i, true_j, mechanism, seed)
                rows.append(row)
                print(f"  dist={row['distance']} {mechanism:9s} seed={seed}  "
                      f"CGP rank={row['cgp_rank']:2d}  EM rank={row['em_rank']:2d}")
    print(f"\ntotal time: {time.perf_counter() - t_start:.1f}s")

    out_path = Path(__file__).resolve().parent.parent / "results" / "benchmark_suite.csv"
    with open(out_path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)
    print(f"wrote {out_path}")

    print("\nTop-1 accuracy (fraction of datasets where the method's #1 pick was the true pair):")
    print(f"{'mechanism':10s} {'distance':9s} {'n':>4s} {'CGP acc':>8s} {'EM acc':>7s}")
    for mechanism in MECHANISMS:
        for distance in sorted(set(r["distance"] for r in rows)):
            subset = [r for r in rows if r["mechanism"] == mechanism and r["distance"] == distance]
            if not subset:
                continue
            cgp_acc = np.mean([r["cgp_top1_correct"] for r in subset])
            em_acc = np.mean([r["em_top1_correct"] for r in subset])
            print(f"{mechanism:10s} {distance:<9d} {len(subset):>4d} {cgp_acc:>8.2f} {em_acc:>7.2f}")


if __name__ == "__main__":
    main()
